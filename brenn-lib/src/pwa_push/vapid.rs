    /// The fixed scalar-1 P-256 test key (secret + signing key). Scalar 1 is
    /// the minimum nonzero valid P-256 scalar; its public key is the generator
    /// point G. Shared so every test that pins bytes against this key builds it
    /// the same way.
    fn scalar_one_key() -> (p256::SecretKey, SigningKey) {
        let mut scalar = [0u8; 32];
        scalar[31] = 1;
        let secret =
            p256::SecretKey::from_slice(&scalar).expect("scalar 1 is a valid P-256 private key");
        let signing_key = SigningKey::from(&secret);
        (secret, signing_key)
    }

