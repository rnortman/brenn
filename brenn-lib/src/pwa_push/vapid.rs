//! VAPID keypair file load and generate.
//!
//! On-disk format (JSON, UTF-8):
//! ```json
//! {
//!   "public_b64url": "<87-char base64url uncompressed P-256 public key>",
//!   "private_b64url": "<43-char base64url of 32-byte P-256 private scalar>"
//! }
//! ```
//!
//! The private key is stored as raw 32-byte P-256 scalar in base64url-no-pad
//! form (43 chars). The public key is stored as uncompressed X9.62 form
//! (0x04 + 32-byte X + 32-byte Y = 65 bytes → 87 chars base64url-no-pad).
//!
//! The file is written with mode 0600 on first generation. The private key
//! never leaves the server.
//!
//! Note: base64url-no-pad of 32 bytes is `ceil(32 * 4/3)` = 43 chars
//! (= 32 * 8 bits / 6 bits per char, rounded up).

use std::os::unix::fs::OpenOptionsExt as _;
use std::path::Path;
use std::sync::Arc;

use base64ct::{Base64UrlUnpadded, Encoding as _};
use p256::ecdsa::SigningKey;
use p256::ecdsa::signature::Signer as _;
use p256::elliptic_curve::sec1::ToEncodedPoint as _;
use serde::{Deserialize, Serialize};

/// Loaded VAPID keypair.
///
/// `public_b64url` is the 87-char base64url-no-pad encoding of the 65-byte
/// uncompressed P-256 public key (0x04 || X || Y). This is the
/// `applicationServerKey` value sent to the browser.
///
#[derive(Clone)]
pub struct VapidKeypair {
    /// 87-char base64url-no-pad uncompressed P-256 public key.
    /// Sent to browser as `applicationServerKey`.
    pub public_b64url: String,
    /// Parsed signing key for constructing VAPID JWT signatures per push.
    /// `Arc` for cheap clone across threads.
    pub signing_key: Arc<SigningKey>,
}

impl std::fmt::Debug for VapidKeypair {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("VapidKeypair")
            .field("public_b64url", &self.public_b64url)
            .field("signing_key", &"<redacted>")
            .finish()
    }
}

/// On-disk JSON representation of the keypair.
#[derive(Serialize, Deserialize)]
struct KeypairFile {
    public_b64url: String,
    private_b64url: String,
}

/// Load the VAPID keypair from `path`, generating and writing it if absent.
///
/// # Panics
///
/// - If the file exists but cannot be read (`std::io::Error`).
/// - If the file exists but cannot be JSON-parsed (corrupt keypair file is
///   an operator configuration error — panic rather than silently drop the
///   file).
/// - If the generated bytes produce an invalid key (should be impossible
///   with a correct `p256` build, but caught defensively).
/// - If the file cannot be written (permissions / filesystem error at
///   startup is a config bug, not a transient error).
pub fn load_or_generate(path: &Path) -> VapidKeypair {
    if path.exists() {
        load(path)
    } else {
        generate_and_write(path)
    }
}

fn load(path: &Path) -> VapidKeypair {
    let content = std::fs::read_to_string(path).unwrap_or_else(|e| {
        panic!(
            "config: [pwa_push].keypair_file at {} is unreadable: {e}",
            path.display()
        )
    });
    parse_keypair_file(&content, path)
}

fn parse_keypair_file(content: &str, path: &Path) -> VapidKeypair {
    let file: KeypairFile = serde_json::from_str(content).unwrap_or_else(|e| {
        panic!(
            "config: [pwa_push].keypair_file at {} is corrupt (JSON parse error): {e}",
            path.display()
        )
    });
    let private_bytes = Base64UrlUnpadded::decode_vec(&file.private_b64url).unwrap_or_else(|e| {
        panic!(
            "config: [pwa_push].keypair_file at {} has invalid private_b64url: {e}",
            path.display()
        )
    });
    // from_slice takes a 32-byte big-endian scalar.
    let secret = p256::SecretKey::from_slice(&private_bytes).unwrap_or_else(|e| {
        panic!(
            "config: [pwa_push].keypair_file at {} has unloadable private key: {e}",
            path.display()
        )
    });
    let signing_key = SigningKey::from(&secret);
    // Re-derive public key from the loaded private key to validate consistency.
    let derived_pub = derive_public_b64url(&secret);
    if derived_pub != file.public_b64url {
        panic!(
            "config: [pwa_push].keypair_file at {} — public_b64url in file does not match \
             the key derived from private_b64url (key material may be corrupt or mismatched)",
            path.display()
        );
    }
    VapidKeypair {
        public_b64url: file.public_b64url,
        signing_key: Arc::new(signing_key),
    }
}

/// Derive the base64url-no-pad uncompressed public key from a secret key.
///
/// Returns the 65-byte uncompressed point (0x04 || X || Y) encoded as
/// base64url-no-pad (87 chars). This is the exact on-disk format.
fn derive_public_b64url(secret: &p256::SecretKey) -> String {
    // to_encoded_point(false) = uncompressed (0x04 + X + Y = 65 bytes).
    let raw = secret.public_key().to_encoded_point(false);
    Base64UrlUnpadded::encode_string(raw.as_bytes())
}

fn generate_and_write(path: &Path) -> VapidKeypair {
    // OsRng is available via p256's elliptic_curve re-export.
    let mut rng = p256::elliptic_curve::rand_core::OsRng;
    let secret = p256::SecretKey::random(&mut rng);
    let private_bytes = secret.to_bytes();
    let public_b64url = derive_public_b64url(&secret);
    let private_b64url = Base64UrlUnpadded::encode_string(&private_bytes);
    let signing_key = SigningKey::from(&secret);

    let file_content = serde_json::to_string_pretty(&KeypairFile {
        public_b64url: public_b64url.clone(),
        private_b64url,
    })
    .expect("serialization of KeypairFile is infallible");

    // Atomic write: write to a temp file in the same directory, then rename.
    // This prevents a partial write from corrupting the keypair file.
    let parent = path.parent().unwrap_or(Path::new("."));
    let tmp_path = parent.join(format!(".vapid.tmp.{}", std::process::id()));

    // Write with mode 0600 (private key never world-readable).
    // `create_new(true)` (O_CREAT | O_EXCL) ensures the file is newly created,
    // so `mode(0o600)` is guaranteed to apply. A pre-existing temp file at this
    // path (e.g. from a stale prior run, or a symlink planted by an attacker)
    // is treated as an invariant violation — operator should remove it manually.
    {
        use std::io::Write as _;
        let mut f = std::fs::OpenOptions::new()
            .write(true)
            .create_new(true)
            .mode(0o600)
            .open(&tmp_path)
            .unwrap_or_else(|e| {
                panic!(
                    "config: failed to create VAPID keypair temp file at {} \
                     (O_CREAT|O_EXCL failed — stale temp file? remove it and restart): {e}",
                    tmp_path.display()
                )
            });
        f.write_all(file_content.as_bytes()).unwrap_or_else(|e| {
            panic!(
                "config: failed to write VAPID keypair to {}: {e}",
                tmp_path.display()
            )
        });
    }

    std::fs::rename(&tmp_path, path).unwrap_or_else(|e| {
        panic!(
            "config: failed to rename VAPID keypair from {} to {}: {e}",
            tmp_path.display(),
            path.display()
        )
    });

    tracing::info!(path = %path.display(), "generated new VAPID keypair");

    VapidKeypair {
        public_b64url,
        signing_key: Arc::new(signing_key),
    }
}

/// Build a VAPID `Authorization` header value for an RFC 8292-conformant JWT.
///
/// Implements ES256 JWT signing in-tree to avoid pulling `jwt-simple` and the
/// `rsa` chain. The JWT structure exactly matches what `web-push-native`'s
/// `with_vapid` produced:
///   - Header: `{"typ":"JWT","alg":"ES256"}`
///   - Claims: `{"aud":"<scheme>://<host>","sub":"<contact>","exp":<unix-secs>}`
///   - Signature: ES256 = ECDSA-P256 + SHA-256, fixed-length 64-byte `r||s`
///     (JOSE encoding, NOT DER/ASN.1).
/// - Returns: `format!("vapid t=<three-part-jwt>, k=<public_b64url>")`
///
/// # Correctness note
///
/// `p256::ecdsa::Signature::to_bytes()` returns the raw 64-byte `r||s` form
/// (IEEE P1363 / JOSE) — not DER. This is the encoding push services require.
/// A DER signature compiles and signs but every push service silently rejects it.
pub fn build_vapid_authorization(
    keypair: &VapidKeypair,
    endpoint: &http::Uri,
    contact: &str,
    valid_duration: std::time::Duration,
) -> String {
    let header_json = r#"{"typ":"JWT","alg":"ES256"}"#;
    let header_b64 = Base64UrlUnpadded::encode_string(header_json.as_bytes());

    // Claims: aud = scheme://host (no port, no path), sub = contact, exp = now + duration.
    // scheme and host must be present — caller guarantees a well-formed push endpoint URI
    // that has already passed validate_endpoint; absence here is an invariant violation.
    let scheme = endpoint
        .scheme_str()
        .expect("VAPID endpoint URI must have a scheme (invariant: validate_endpoint passed)");
    let host = endpoint
        .host()
        .expect("VAPID endpoint URI must have a host (invariant: validate_endpoint passed)");
    let aud = format!("{scheme}://{host}");

    let exp = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .expect("system clock is after 1970")
        .as_secs()
        + valid_duration.as_secs();

    // aud, sub, exp per RFC 8292. (iat/nbf/jti are optional and omitted.)
    // Use serde_json to build the claims object so that string values are correctly
    // JSON-escaped regardless of content — prevents silent claims corruption if the
    // operator-supplied subject or the endpoint host contains `"` or `\`.
    #[derive(serde::Serialize)]
    struct VapidClaims<'a> {
        aud: &'a str,
        sub: &'a str,
        exp: u64,
    }
    let claims_json = serde_json::to_string(&VapidClaims {
        aud: &aud,
        sub: contact,
        exp,
    })
    .expect("VapidClaims serialization is infallible");
    let claims_b64 = Base64UrlUnpadded::encode_string(claims_json.as_bytes());

    let signing_input = format!("{header_b64}.{claims_b64}");

    // ES256 sign: ECDSA-P256 + SHA-256. `.sign()` hashes internally.
    // `.to_bytes()` returns the 64-byte fixed-length r||s (JOSE form, NOT DER).
    let sig: p256::ecdsa::Signature = keypair.signing_key.sign(signing_input.as_bytes());
    let sig_b64 = Base64UrlUnpadded::encode_string(&sig.to_bytes());

    let jwt = format!("{signing_input}.{sig_b64}");

    format!("vapid t={jwt}, k={}", keypair.public_b64url)
}

